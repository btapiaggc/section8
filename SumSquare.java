package gitHubSection_8;
public class WhileSumSquare {
	public static void main(String[] args) {
	
		int count= 0;
		int result = 0;
		while (count < 33);
		{
			count += 3;
			
			result = count * count;
			
			
			
		System.out.println("Cumulation result: " + result + " : " + count + " * "  + count);
			
		}		
		
	
	
	

}
	}
